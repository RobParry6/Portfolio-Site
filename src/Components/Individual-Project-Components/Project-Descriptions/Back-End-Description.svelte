<p>This aim of this Back End project was to create an back end RESTful API which emulates a database for a games reviews sites. The API itself has a number of endpoints where multiple CRUD methods can be sent to in order to retrieve information from the database or in the case of POST and PATCH requests, alter the database in the desired way.</p>

<p>The API was created using the express package in order to create the necassary endpoints (and deal with the necassary error handling). Best practices that were employed throughout the project include using test driven development (TTD), to write the code efficiently and handle errors as the complexity of the endpoints built up, MVC, in order to better structure the API, and agile concepts in order to manage workflow. </p>

