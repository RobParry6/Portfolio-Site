<p>Funbelievable Reviews is a front end project that emulate a games reviews site where people come together to review their favourite board games. Users who are regestisted to the app are able to comment on and endorse on reviews, whereas people who are only visiting to the site are only able to view the reviews.</p>
    
<p>The project was created using React and made use of an API that had been created for the back end project. The app uses react route dom to provide navigation amd accessability for a more user friendly experience.</p>

<p>For navigation around the app there is a filter bar which has a number of options for filtering the articles and also links to the url for those who wish navigate via the url rather than the links on the page.</p>

<p> A link to the Github repository can be found here: <a href="https://github.com/RobParry6/Funbelievable-Reviews">Front End Project</a></p>
    
